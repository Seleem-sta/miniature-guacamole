export const getProductById = (id: string): Product | undefined => {
  return products.find(p => p.id === id);
};

export const getProductsByCategory = (category: string): Product[] => {
  if (category === 'all') return products;
  return products.filter(p => p.category === category);
};

export const getProductsBySubcategory = (category: string, subcategory: string): Product[] => {
  return products.filter(p => p.category === category && p.subcategory === subcategory);
};

export const getNewArrivals = (): Product[] => {
  return products.filter(p => p.isNew).slice(0, 8);
};

export const getSaleItems = (): Product[] => {
  return products.filter(p => p.isSale).slice(0, 8);
};

export const getFeaturedProducts = (): Product[] => {
  return products.filter(p => p.rating >= 4.8).slice(0, 8);
};
